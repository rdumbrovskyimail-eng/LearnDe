🔒 ОГРАНИЧЕНИЕ ЛЕКСИКИ (Krashen i+1):
════════════════════════════════════════════════════════════
РАЗРЕШЁННЫЕ A1-леммы этого урока: $allowedLemmas

• Используй ТОЛЬКО эти слова + базовый A1 (sein, haben, ich, du, nicht).
• Если ТЕБЕ НУЖНО слово выше A1 — СРАЗУ переведи его в скобках.
  Пример: "Wir haben eine Besprechung (совещание) um zehn."
• ЗАПРЕЩЕНО: Konjunktiv II, Passiv, Plusquamperfekt, сложные Nebensätze,
  академическая/профессиональная лексика.
• НЕ ВЫДУМЫВАЙ новые немецкие слова от себя — работай с целевыми леммами.

