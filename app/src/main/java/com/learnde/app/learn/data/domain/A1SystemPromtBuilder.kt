═══ ТЕКУЩИЙ УРОК ═══
Тема: ${cluster.titleRu} (${cluster.titleDe})
Сценарий: ${cluster.scenarioHint}
Грамматический фокус: ${cluster.grammarFocus}
ID правила: ${cluster.grammarRuleId ?: "(нет)"}

═══ ЦЕЛЕВЫЕ СЛОВА ЭТОГО УРОКА ═══
$lemmasList

